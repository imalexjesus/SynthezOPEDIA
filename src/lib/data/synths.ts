export const groupedSynths = synths.reduce((acc, synth) => {
  const key = `${synth.brand} - ${synth.series}`;
  if (!acc[key]) acc[key] = [];
  acc[key].push(synth);
  return acc;
}, {} as Record<string, SynthModel[]>);

